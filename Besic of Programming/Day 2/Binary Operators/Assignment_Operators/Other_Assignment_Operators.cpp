#include <iostream>
using namespace std;

int main(){

    // Tere are operations which we perform thes operations can be done using
    // shortcut. For that there are assignment operators 


    // (+=)
    int num_1 = 5;

    // num_1 = num_1 + 10;
    num_1 += 10; // use this insted

    cout << num_1 << endl;


    // (-=)
    int num_2 = 5;

    // num_2 = num_2 - 2;
    num_2 -= 2; // use this insted

    cout << num_2 << endl;


    // (*=)
    int num_3 = 5;

    // num_3 = num_3 * 2;
    num_3 *= 2; // use this insted

    cout << num_3 << endl;


    // (/=)
    int num_4 = 10;

    // num_4 = num_4 / 2;
    num_4 /= 2; // use this insted

    cout << num_4 << endl;


    // (%=)
    int num_5 = 22;

    // num_5 = num_5 % 5;
    num_5 %= 5; // use this insted

    cout << num_5 << endl;


    return 0;
}